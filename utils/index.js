const addPost = async postItem => {
  try {
    const posts = (await getPosts()) || [];

    postItem.id = new Date().getTime();
    postItem.date = new Date();
    posts.push(postItem);

    await AsyncStorage.setItem('posts', JSON.stringify(posts));
    return 'success';
  } catch (error) {
    console.log(error);
    return 'error';
  }
};

const getPosts = async () => {
  try {
    const json = await AsyncStorage.getItem('posts');
    if (json !== null) {
      // We have data!!
      return JSON.parse(json);
    } else return [];
  } catch (error) {
    // Error retrieving data
  }
};

export {validateEmail, addPost, getPosts};
